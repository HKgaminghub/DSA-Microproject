int length(char *str)
{
    int i;
    for(i=0;str[i]!='\0';i++);
    return i;
}

void copy(char *str1, char *str2)
{
    int i;
    for(i=0;i<length(str2);i++)
    {
        str1[i]=str2[i];
    }
    str1[i]='\0';
}

char * ToLower(char *str)
{
    int i=0;
    char *tmp = malloc(sizeof(char)*length(str)+1);

    while(str[i] != '\0')
    {
        if(str[i]>='A' && str[i]<='Z')
        {
            tmp[i]=str[i]+32;
        }
        else
        {
            tmp[i]=str[i];
        }
        i++;
    }
    tmp[i]='\0';
    return tmp;
}

int compare(char *str1, char *str2)
{
    int i = 0;
    char *lower1 = ToLower(str1);
    char *lower2 = ToLower(str2);

    while(lower1[i] != '\0' && lower2[i] != '\0')
    {
        if(lower1[i] < lower2[i])
        {
            free(lower1);
            free(lower2);
            return -1;
        }
        else if(lower1[i] > lower2[i])
        {
            free(lower1);
            free(lower2);
            return 1;
        }
        i++;
    }

    if(lower1[i] != '\0')
    {
        free(lower1);
        free(lower2);
        return 1;
    }
    else if(lower2[i] != '\0')
    {
        free(lower1);
        free(lower2);
        return -1;
    }
    else
    {
        free(lower1);
        free(lower2);
        return 0;
    }
}

